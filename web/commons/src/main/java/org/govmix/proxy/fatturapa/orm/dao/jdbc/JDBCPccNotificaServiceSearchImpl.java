	private PccNotifica _get(JDBCServiceManagerProperties jdbcProperties, Logger log, Connection connection, ISQLQueryObject sqlQueryObject, Long tableId, org.openspcoop2.generic_project.beans.IDMappingBehaviour idMappingResolutionBehaviour) throws NotFoundException, MultipleResultException, NotImplementedException, ServiceException, Exception {
	
		org.openspcoop2.generic_project.dao.jdbc.utils.JDBCPreparedStatementUtilities jdbcUtilities = 
					new org.openspcoop2.generic_project.dao.jdbc.utils.JDBCPreparedStatementUtilities(sqlQueryObject.getTipoDatabaseOpenSPCoop2(), log, connection);
		
		ISQLQueryObject sqlQueryObjectGet = sqlQueryObject.newSQLQueryObject();
				
		PccNotifica pccNotifica = new PccNotifica();
		

		// Object pccNotifica
		ISQLQueryObject sqlQueryObjectGet_pccNotifica = sqlQueryObjectGet.newSQLQueryObject();
		sqlQueryObjectGet_pccNotifica.setANDLogicOperator(true);
		sqlQueryObjectGet_pccNotifica.addFromTable(this.getPccNotificaFieldConverter().toTable(PccNotifica.model()));
		sqlQueryObjectGet_pccNotifica.addSelectField("id");
		sqlQueryObjectGet_pccNotifica.addSelectField(this.getPccNotificaFieldConverter().toColumn(PccNotifica.model().STATO_CONSEGNA,true));
		sqlQueryObjectGet_pccNotifica.addSelectField(this.getPccNotificaFieldConverter().toColumn(PccNotifica.model().DATA_CREAZIONE,true));
		sqlQueryObjectGet_pccNotifica.addSelectField(this.getPccNotificaFieldConverter().toColumn(PccNotifica.model().DATA_CONSEGNA,true));
		sqlQueryObjectGet_pccNotifica.addWhereCondition("id=?");

		// Get pccNotifica
		pccNotifica = (PccNotifica) jdbcUtilities.executeQuerySingleResult(sqlQueryObjectGet_pccNotifica.createSQLQuery(), jdbcProperties.isShowSql(), PccNotifica.model(), this.getPccNotificaFetch(),
			new JDBCObject(tableId,Long.class));


		if(idMappingResolutionBehaviour==null ||
			(org.openspcoop2.generic_project.beans.IDMappingBehaviour.ENABLED.equals(idMappingResolutionBehaviour) || org.openspcoop2.generic_project.beans.IDMappingBehaviour.USE_TABLE_ID.equals(idMappingResolutionBehaviour))
		){
			// Object _pccNotifica_pccTraccia (recupero id)
			ISQLQueryObject sqlQueryObjectGet_pccNotifica_pccTraccia_readFkId = sqlQueryObjectGet.newSQLQueryObject();
			sqlQueryObjectGet_pccNotifica_pccTraccia_readFkId.addFromTable(this.getPccNotificaFieldConverter().toTable(org.govmix.proxy.fatturapa.orm.PccNotifica.model()));
			sqlQueryObjectGet_pccNotifica_pccTraccia_readFkId.addSelectField("id_pcc_traccia");
			sqlQueryObjectGet_pccNotifica_pccTraccia_readFkId.addWhereCondition("id=?");
			sqlQueryObjectGet_pccNotifica_pccTraccia_readFkId.setANDLogicOperator(true);
			Long idFK_pccNotifica_pccTraccia = (Long) jdbcUtilities.executeQuerySingleResult(sqlQueryObjectGet_pccNotifica_pccTraccia_readFkId.createSQLQuery(), jdbcProperties.isShowSql(),Long.class,
					new JDBCObject(pccNotifica.getId(),Long.class));
			
			org.govmix.proxy.fatturapa.orm.IdTraccia id_pccNotifica_pccTraccia = null;
			if(idMappingResolutionBehaviour==null || org.openspcoop2.generic_project.beans.IDMappingBehaviour.ENABLED.equals(idMappingResolutionBehaviour)){
				id_pccNotifica_pccTraccia = ((JDBCPccTracciaServiceSearch)(this.getServiceManager().getPccTracciaServiceSearch())).findId(idFK_pccNotifica_pccTraccia, false);
			}else{
				id_pccNotifica_pccTraccia = new org.govmix.proxy.fatturapa.orm.IdTraccia();
			}
			id_pccNotifica_pccTraccia.setId(idFK_pccNotifica_pccTraccia);
			pccNotifica.setIdTraccia(id_pccNotifica_pccTraccia);
		}

		if(idMappingResolutionBehaviour==null ||
			(org.openspcoop2.generic_project.beans.IDMappingBehaviour.ENABLED.equals(idMappingResolutionBehaviour) || org.openspcoop2.generic_project.beans.IDMappingBehaviour.USE_TABLE_ID.equals(idMappingResolutionBehaviour))
		){
			// Object _pccNotifica_dipartimento (recupero id)
			ISQLQueryObject sqlQueryObjectGet_pccNotifica_dipartimento_readFkId = sqlQueryObjectGet.newSQLQueryObject();
			sqlQueryObjectGet_pccNotifica_dipartimento_readFkId.addFromTable(this.getPccNotificaFieldConverter().toTable(org.govmix.proxy.fatturapa.orm.PccNotifica.model()));
			sqlQueryObjectGet_pccNotifica_dipartimento_readFkId.addSelectField("id_dipartimento");
			sqlQueryObjectGet_pccNotifica_dipartimento_readFkId.addWhereCondition("id=?");
			sqlQueryObjectGet_pccNotifica_dipartimento_readFkId.setANDLogicOperator(true);
			Long idFK_pccNotifica_dipartimento = (Long) jdbcUtilities.executeQuerySingleResult(sqlQueryObjectGet_pccNotifica_dipartimento_readFkId.createSQLQuery(), jdbcProperties.isShowSql(),Long.class,
					new JDBCObject(pccNotifica.getId(),Long.class));
			
			org.govmix.proxy.fatturapa.orm.IdDipartimento id_pccNotifica_dipartimento = null;
			if(idMappingResolutionBehaviour==null || org.openspcoop2.generic_project.beans.IDMappingBehaviour.ENABLED.equals(idMappingResolutionBehaviour)){
				id_pccNotifica_dipartimento = ((JDBCDipartimentoServiceSearch)(this.getServiceManager().getDipartimentoServiceSearch())).findId(idFK_pccNotifica_dipartimento, false);
			}else{
				id_pccNotifica_dipartimento = new org.govmix.proxy.fatturapa.orm.IdDipartimento();
			}
			id_pccNotifica_dipartimento.setId(idFK_pccNotifica_dipartimento);
			pccNotifica.setIdDipartimento(id_pccNotifica_dipartimento);
		}

        return pccNotifica;  
	
	} 
	
