	private Ente _get(JDBCServiceManagerProperties jdbcProperties, Logger log, Connection connection, ISQLQueryObject sqlQueryObject, Long tableId, org.openspcoop2.generic_project.beans.IDMappingBehaviour idMappingResolutionBehaviour) throws NotFoundException, MultipleResultException, NotImplementedException, ServiceException, Exception {
	
		org.openspcoop2.generic_project.dao.jdbc.utils.JDBCPreparedStatementUtilities jdbcUtilities = 
					new org.openspcoop2.generic_project.dao.jdbc.utils.JDBCPreparedStatementUtilities(sqlQueryObject.getTipoDatabaseOpenSPCoop2(), log, connection);
		
		ISQLQueryObject sqlQueryObjectGet = sqlQueryObject.newSQLQueryObject();
				
		Ente ente = new Ente();
		

		// Object ente
		ISQLQueryObject sqlQueryObjectGet_ente = sqlQueryObjectGet.newSQLQueryObject();
		sqlQueryObjectGet_ente.setANDLogicOperator(true);
		sqlQueryObjectGet_ente.addFromTable(this.getEnteFieldConverter().toTable(Ente.model()));
		sqlQueryObjectGet_ente.addSelectField("id");
		sqlQueryObjectGet_ente.addSelectField(this.getEnteFieldConverter().toColumn(Ente.model().NOME,true));
		sqlQueryObjectGet_ente.addSelectField(this.getEnteFieldConverter().toColumn(Ente.model().ID_PCC_AMMINISTRAZIONE,true));
		sqlQueryObjectGet_ente.addSelectField(this.getEnteFieldConverter().toColumn(Ente.model().CF_AUTH,true));
		sqlQueryObjectGet_ente.addSelectField(this.getEnteFieldConverter().toColumn(Ente.model().DESCRIZIONE,true));
		sqlQueryObjectGet_ente.addWhereCondition("id=?");

		// Get ente
		ente = (Ente) jdbcUtilities.executeQuerySingleResult(sqlQueryObjectGet_ente.createSQLQuery(), jdbcProperties.isShowSql(), Ente.model(), this.getEnteFetch(),
			new JDBCObject(tableId,Long.class));



		
        return ente;  
	
	} 
	
